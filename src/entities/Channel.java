package entities;

public class Channel {
	
	private String channelName;
	private String channelBand;
	private double videoFrequency;
	private double audioFrequency;
	private String channelChargeType;
	private String channelTransmissionType;
	private double channelCharge;
	private String channelEnableOrDisable;
	
	public String getChannelName() {
		return channelName;
	}
	
	public void setChannelName(String channelName) {
		this.channelName = channelName;
	}
	public String getChannelBand() {
		return channelBand;
	}
	public void setChannelBand(String channelBand) {
		this.channelBand = channelBand;
	}
	public double getVideoFrequency() {
		return videoFrequency;
	}
	public void setVideoFrequency(double videoFrequency) {
		this.videoFrequency = videoFrequency;
	}
	public double getAudioFrequency() {
		return audioFrequency;
	}
	public void setAudioFrequency(double audioFrequency) {
		this.audioFrequency = audioFrequency;
	}
	public String getChannelChargeType() {
		return channelChargeType;
	}
	public void setChannelChargeType(String channelChargeType) {
		this.channelChargeType = channelChargeType;
	}
	public String getChannelTransmissionType() {
		return channelTransmissionType;
	}
	public void setChannelTransmissionType(String channelTransmissionType) {
		this.channelTransmissionType = channelTransmissionType;
	}
	public double getChannelCharge() {
		return channelCharge;
	}
	public void setChannelCharge(double channelCharge) {
		this.channelCharge = channelCharge;
	}
	public String getChannelEnableOrDisable() {
		return channelEnableOrDisable;
	}
	public void setChannelEnableOrDisable(String channelEnableOrDisable) {
		this.channelEnableOrDisable = channelEnableOrDisable;
	}


}
